/***
=== Linked List Big O complexity ===

Push Front: O(1) - Constant Time
Push Back: O(N) - Linear Time
  If it has the tail: O(1) - Constant Time
Push in the middle: O(N) - Linear Time
Remove: O(N) - Linear Time
  Remove of head or tail: O(1) - Constant Time
Is Empty: O(1) - Constant Time
Size: O(N) - Linear Time
Search: O(N) - Linear Time
  Get head or tail: O(1) - Constant Time
***/
